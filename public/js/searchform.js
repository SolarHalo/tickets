	function check( ){
		var keyword = $("#keyword").val();
		var location = $("#location").val();
		var fromDate = $("#fromDate").val();
		var toDate = $("#toDate").val();
		
		if(keyword == "Search by keyword"){
			$("#keyword").val("");
		}
		
		if("Search by location" == location){
			$("#location").val("");
		}
		
		if("Date From" == fromDate){
			$("#fromDate").val("");
		}
		
		if("Date To" == toDate){
			$("#toDate").val("");
		}
		
		return true;
	}
	function init(){

		if($("#keyword").val() == ""){
			$("#keyword").val("Search by keyword");
		}
	 
		if("" == $("#location").val()){
			$("#location").val("Search by location");
		}
		
		if("" == $("#fromDate").val()){
			$("#fromDate").val("Date From");
		}
		
		if("" == $("#toDate").val()){
			$("#toDate").val("Date To");
		}
	}